    <p>Com base na revisão de literatura, ao cotejar as etapas dos métodos e a nomenclatura das etapas, percebeu-se a existência das seguintes fases:</p>
    <ul>
      <li><strong>Pré-produção:</strong>
        <ol>
          <li>Compreensão: etapa de levantamento de dados sobre o contexto, os usuários, a tecnologia, os recursos e requisitos do projeto, verificada sob os termos: nível heurístico, ideação, pré-produção, planejamento de requisitos e documentação.</li>
          <li>Estruturação: etapa para a definição dos objetivos de aprendizagem, concepção e design da solução baseada em jogos digitais, identificada sob o uso dos termos: definição do conceito instrucional, definição do conceito do jogo, definição dos objetivos de aprendizagem, design, nível heurístico, concepção, pré-produção, desenvolvimento do conceito, gerenciamento de produção, estruturação do modelo de conteúdo, modelo de tarefa e modelo de evidência.</li>
        </ol>
      </li>
      <li><strong>Produção:</strong>
        <ol start="3">
          <li>Produção: passo em que ocorre a prototipagem e o desenvolvimento dos componentes da solução para a aprendizagem baseada em jogos digitais, aferido pelos vocábulos: desenvolvimento, nível produtivo, prototipagem, atualização, produção e realização das tarefas.</li>
        </ol>
      </li>
      <li><strong>Pós-produção:</strong>
        <ol start="4">
          <li>Distribuição: estágio em que a solução baseada em jogos digitais é disponibilizada para professores e estudantes, constatado ante os termos: implementação, nível empírico, distribuição, gerenciamento da comunidade e modelo de tarefa.</li>
          <li>Aplicação: etapa em que a proposta baseada em jogos digitais é utilizada pelos estudantes, relatada como: implementação, nível de validação, gerenciamento da comunidade, suporte, treinamento e modelo de tarefa.</li>
          <li>Avaliação: fase que envolve a verificação dos resultados da aplicação da abordagem da aprendizagem baseada em jogos digitais, observada como: avaliação, resultados de aprendizagem, nível de validação, controle de qualidade e modelo de evidência.</li>
        </ol>
      </li>
    </ul>

    <p>Percebeu-se que algumas abordagens não possuem foco na discussão da produção do jogo em si, mas no modelo para integração com a educação, ou ainda, no modelo conceitual, privilegiando as primeiras etapas do ciclo. Ademais, detectou-se que a macro etapa de pós-produção contém mais fases que as demais, enquanto a de produção conta com menos.</p>
    <p>Notou-se que algumas abordagens concentram-se na etapa de estruturação e carecem de previsão do restante do ciclo projetual, sobretudo das etapas de produção, distribuição e aplicação. Isso demonstra uma lacuna projetual que pode significar pouca comunicação e laços enfraquecidos entre educadores e designers. Além disso, foi constatado que alguns métodos não preveem etapa de compreensão, que visa entender o usuário, seu contexto tecnológico e pedagógico. Essa apuração indica que há um risco projetual em produzir uma solução que pode não ser adequada e reforça a hipótese de que o estudante tem participação limitada no processo de design.</p>
