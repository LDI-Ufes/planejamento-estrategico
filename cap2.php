 <figure class="retrato lateral">
   <img width="270" height="270" src="img/ilustra_2.png" alt=""/>
 </figure>

  
  <ol>
    <li><strong>Escolha de um tema:</strong> define como o jogo cativará o jogador, dos pontos de vista emocional e motivacional, expressa a meta, o ambiente, as condições e eventos.</li>
    <li><strong>Pesquisa e preparação:</strong> etapa para pesquisar ideias similares e esforços anteriores, reunir ideias e quesitos desejáveis.</li>
    <li><strong>Processo de design:</strong> define a estrutura de interação, com <em lang="en">inputs </em>(meios de entrada para interagir com o jogo) e <em lang="en">outputs</em> (meios de saída, que fornecem <em lang="en">feedback</em> sobre as ações), enquanto a estruturação do jogo deve prever as ações e possibilidades de resolução de desafios.</li>
    <li><strong>Pré-programação:</strong> etapa para preparar a estrutura de programação que traduzirá o esquema de entradas e saídas e o sistema do jogo em um produto. Para tal, é preciso definir tarefas e seu fluxo de realização.</li>
    <li><strong>Programação:</strong> fase de tradução das estruturas de design definidas em produto jogável, por meio de programação em códigos de linguagens.</li>
    <li><strong>Testes:</strong> estágio de realização de testes para refinar o jogo e averiguar se há falhas na programação. Esses testes podem ser realizados de modo interno, apenas com a equipe e, posteriormente, com membros externos. Ao final dessa etapa, o jogo será preparado para lançamento.</li>
    <li><strong><em lang="en"> Post mortem</em>:</strong> após o lançamento do jogo, dedica-se atenção às críticas do público.</li>
  </ol>

  <p>Novak (2010, p. 340) pontua questões ao descrever o processo de design de um jogo, que compreendem indagações sobre os componentes de cada etapa, as maneiras de gerenciar equipes e as formas de documentar as etapas. São evidenciados os seguintes passos do processo: conceito, pré-produção, protótipo, produção e pós-produção.</p>
  <ol>
    <li><strong>Conceito:</strong> envolve a criação e o registro da ideia do jogo, de modo sucinto e objetivo.</li>
    <li><strong>Pré-produção:</strong> nessa fase ocorre a documentação do jogo, por meio da elaboração dos documentos: de conceito, da proposta do jogo, de design do jogo, documento técnico de design, guia de estilo da arte, plano de projeto e plano de testes (NOVAK, 2010, p. 363).
      <ol type="a">
        <li>Documento de conceito: o documento do conceito informa, de modo pontual, o objetivo e o diferencial do jogo, a experiência do jogador e a viabilidade de execução do projeto.</li>
        <li>Documento de proposta do jogo: apresenta informações complementares sobre o que atrairá e manterá o público, o modo de jogar, os recursos, a tecnologia e os aspectos visuais.</li>
        <li>Documento de design do jogo (<em>game design document</em>): é o mais longo e detalhado documento para uso interno da equipe como guia de referência, com informações sobre o modo de jogar, a história, a interface e as regras do jogo.</li>
        <li>Guia de estilo da arte: estabelece o aspecto visual do jogo e serve como referência para a produção.</li>
        <li>Documento técnico de design: descreve aspectos e funções do <em>software</em> ou motor de jogo para guiar a produção de transposição do conceito em um projeto jogável.</li>
        <li>Plano de projeto: envolve o cronograma das atividades e planejamento de recursos ao longo do desenvolvimento.</li>
        <li>Plano de testes: envolve procedimentos para verificar o funcionamento e revisar o jogo.</li>
      </ol>
    </li>
    <li><strong>Protótipo:</strong> etapa que pode envolver a criação de protótipo analógico e digital, sendo este último fundamental para demonstrar e traduzir a ideia para o ambiente interativo.</li>
    <li><strong>Produção:</strong> fase de desenvolvimento e elaboração das versões alfa, em que o jogo pode ser jogado do início ao fim, com interface básica e elementos temporários, com foco em dar acabamento a elementos e realizar ajustes; beta, com a conclusão da implementação e correção de problemas; e ouro, em que o produto é fabricado, testado e lançado.</li>
    <li><strong>Pós-produção:</strong> fase de lançamento de versões para melhorias, correções, atualizações, conteúdos adicionais ou expansões.</li>
  </ol>

 

