  <p>Acerca de métodos ágeis, Novak (2010, p. 354) corrobora que o processo de desenvolvimento de um jogo assemelha-se ao de um <em lang="en">software</em>, sendo esse modelo chamado de processo de desenvolvimento iterativo, que apresenta as seguintes fases: design, protótipo e avaliação. A autora assegura que a essência desse modelo é o refinamento contínuo do processo.</p>
  <p>Fullerton (2014, p. 17) apresenta o “<em lang="en">Iterative process</em>” e explica que “iteração” é um processo cíclico de design, teste e avaliação dos resultados, ao longo do desenvolvimento de um jogo. Trata-se de desenvolvimento ágil – um método para o desenvolvimento de <em lang="en">software</em> –, que possui uma variante popular entre designers de jogos, denominada “Scrum”. As etapas do processo apresentado pela autora são seis, a saber: </p>
  <ol>
    <li><strong><em lang="en">Brainstorm</em>:</strong> com o objetivo de definir metas, conceito ou mecânica de jogo e documentar as ideias.</li>
    <li><strong>Protótipo físico:</strong> elaborar um protótipo jogável de papel, testá-lo e descrever as funções do jogo em um documento de poucas páginas.</li>
    <li><strong>Apresentação:</strong> nessa etapa se apresenta o protótipo, a fim de demonstrar a jogabilidade e os aspectos visuais para arrecadar fundos e obter <em lang="en">feedback</em> sobre a proposta.</li>
    <li><strong>Protótipo digital:</strong> envolve criação de um protótipo para demonstrar a jogabilidade, com foco no sistema e suas funcionalidades. </li>
    <li><strong>Documentação:</strong> registro das decisões, funções e dos aspectos técnicos do jogo para guiar a produção.</li>
    <li><strong>Produção:</strong> etapa para tornar o que foi documentado palpável.</li>
  </ol>
  <p>Esse método organiza equipes em grupos menores e funcionais, que priorizam curtas tarefas diárias, de modo iterativo. As iterações e revisões periódicas exigem comunicação frequente. Para a autora, trata-se de um método apropriado ao desenvolvimento de jogos, pois proporciona mudanças com fluidez para resolver os problemas que surgem no processo de design (FULLERTON, 2014, p. 413). </p>
  <p>Para exemplificar, a autora explica o processo: </p>
  <ol type="a">
    <li>Escolha de metas para a experiência do jogador;</li>
    <li>Concepção de uma ideia ou sistema;</li>
    <li>Formalização da ideia por meio de documentação ou prototipagem;</li>
    <li>Avaliar os resultados. Caso sejam reprovados, o processo retorna ao passo inicial; se necessário, são feitos ajustes e novos testes; caso sejam aprovados, esse processo de iteração encerra e outro é iniciado.</li>
  </ol>
  <p>Irish (2005, p. 19) afirma que o modelo “<em lang="en">Iterate until you drop”</em> é flexível e proporciona adaptações e mudanças ao longo do processo de desenvolvimento. Nessa abordagem, após definidos os requisitos de design do jogo, são determinados os de nível inferior. Cada componente é codificado e testado para, em seguida, o sistema com o componente ser testado e, por fim, integrado ao código do jogo. Após cada requisito ter seu ciclo de desenvolvimento completo, outro se inicia.</p>
