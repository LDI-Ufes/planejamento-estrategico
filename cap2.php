  <p>Bates (2004, p. 218) complementa que o desenvolvimento ágil privilegia pessoas e interações, ao invés de processos e ferramentas; um programa em funcionamento, ao invés de documentação; colaboração com o cliente, ao invés de negociação por contrato; responder a mudanças, ao invés de seguir o plano. Para tanto, é preciso compreender os objetivos projetuais, desenvolver um plano e cronograma técnico, entender os problemas que surgirão e trabalhar para evitá-los. O autor acrescenta que o desenvolvimento ágil é útil em projetos em que nem todos os requisitos são compreendidos desde o início, ou tecnologias devem ser criadas, ou ainda, em que o cliente está envolvido. </p>
  <p>Por fim, o autor pondera que projetos em que os requisitos são conhecidos e a tecnologia é estável, sequências e extensões podem ter um desenvolvimento adequado com metodologias tradicionais. O “modelo cascata” conta com uma progressão ordenada e funciona melhor com tudo bem definido desde o início, sem prever mudanças. Seu fluxo envolve o desenvolvimento do conceito; análise de requisitos; design estrutural; detalhamento do design; codificação e depuração; e testes (BATES, 2004, p. 225).</p>
  <p>A partir da pesquisa bibliográfica, foram constatadas algumas semelhanças entre as fases do processo de design. Alguns autores dividem o processo projetual em pré-produção, produção e pós-produção, enquanto outros descrevem mais etapas. Independente da linearidade ou iteratividade no processo, as etapas principais presentes nos métodos consultados são: </p>
  <ul>
    <li><strong>Pré-produção:</strong>
      <ol>
        <li>Conceituação: fase conceitual do processo, com ênfase na definição de requisitos projetuais e do conceito do jogo, verificada sob os temos: escolha de um tema, conceito, desenvolvimento de conceito, concepção, pré-produção, design, definição de requisitos, análise de requisitos, <em lang="en">brainstorm.</em></li>
        <li>Estruturação: etapa de organização dos elementos necessários, a partir do conceito, em que é estruturado o projeto, relatada sob os temos: pesquisa e preparação, processo de design, design, pré-programação, pré-produção, protótipo, concepção, definição de requisitos, <em lang="en">brainstorm</em>, protótipo físico, apresentação, protótipo digital, documentação, detalhamento do design.</li>
      </ol>
    </li>

    <li><strong>Produção:</strong>
      <ol start="3">
        <li>Produção: fase de execução do planejamento feito nas etapas anteriores e desenvolvimento do sistema e da produção e implementação dos elementos do jogo, apresentada pelos termos: programação, produção, elaboração, codificação e teste, codificação e depuração.</li>
        <li>Testes: estágio em que o jogo e os componentes implementados passam por diversos testes e melhorias, conferido pelo uso dos termos: testes, produção, elaboração, produção, desenvolvimento, codificação e teste, codificação e depuração.</li>
      </ol>
    </li>

    <li><strong>Pós-produção:</strong>
      <ol start="5">
        <li>Lançamento: passo existente, constatado e considerado após a conclusão do desenvolvimento e da produção do jogo, não relatado como parte do processo de design, segundo a literatura consultada, pois essa é uma atribuição do <em lang="en">publisher</em>.</li>
        <li>Manutenção: etapa do processo que ocorre após o lançamento e compreende o acompanhamento do uso para verificar se são necessários ajustes ou atualizações no jogo, aferida perante os termos: <em lang="en">post mortem</em>, pós-produção, aperfeiçoamento e desenvolvimento.</li>
      </ol>
    </li>

