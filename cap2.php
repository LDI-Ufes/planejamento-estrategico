  <p>Kiili (2005, p. 18) propõe um modelo, intitulado “<em lang="en">Experimental gaming model</em>” (modelo de jogo experimental), para vincular a jogabilidade com aprendizagem experimental e facilitar a experiência de fluxo. Nesse modelo, a aprendizagem é um processo cíclico, por meio da experiência no jogo – trata-se de um ciclo de experiência e um banco de desafios. Os desafios baseados nos objetivos educacionais são o centro do sistema, que é responsável por manter a motivação e o engajamento, por meio do fornecimento de desafios. Tais desafios demandam soluções, por parte dos estudantes, em etapas de criatividade, geração de ideias.</p>
  <p>Ao testar as soluções, há os resultados das ações. Nesse sentido, o jogo fornece <em lang="en">feedback</em> ao jogador que, em observação e reflexão, irá estruturar novas e melhores soluções aos desafios, que se adequarão ao nível do jogador, que expandiu seu conhecimento. Para iniciar um novo ciclo, o sistema fornece desafios adequados ao seu nível de habilidades e mantém a experiência de fluxo (KIILI, 2005, p. 19).</p>
