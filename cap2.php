  <p>Kapp, Blair e Mesch (2014, p. 145) apresentam um processo de desenvolvimento sob o título “<em lang="en">Serious game development process</em>” e dividem a produção de um jogo educacional em três etapas: pré-produção, produção e distribuição. </p>
  <ul>
    <li><strong>Pré-produção: </strong>
      <ol>
        <li>Desenvolvimento do conceito: etapa de planejamento do conceito do projeto, com o design instrucional e o design baseado em jogos digitais, que envolve a análise do público, a definição dos objetivos de aprendizagem, os resultados desejados, métodos de avaliação e o processo criativo de definição preliminar da história do jogo, seus desafios e níveis. </li>
        <li>Planejamento de requisitos para a produção: fase em que são considerados fatores como a tecnologia, as especificações técnicas, funcionais e do sistema, o planejamento de recursos, plano de desenvolvimento, requisitos e as características do jogo, o <em lang="en">level design</em>, os fluxogramas de telas e as ações, os elementos de arte, de personagem e cenários, as ferramentas para o desenvolvimento, o planejamento de testes e os ciclos de avaliação e implementação.</li>
        <li>Documentação: fase de registro das informações definidas nas etapas anteriores, a fim de guiar a produção.</li>
      </ol>
    </li>
    <li><strong>Produção: </strong>
      <ol start="4">
        <li>Gerenciamento de produção: etapa de gestão de projeto, para garantir que o projeto desenvolvido de modo iterativo mantenha unidade, fluxo e comunicação entre os membros da equipe.</li>
        <li>Design: abrange o design instrucional, Aprendizagem Baseada em Jogos Digitais e o Design de Jogos, conforme definidos anteriormente, na etapa de desenvolvimento do conceito.</li>
        <li>Engenharia: responsável pela programação, pelo desenvolvimento e pela implementação do jogo.</li>
        <li>Arte: compreende a criação dos elementos sonoros e visuais, tais como personagem, cenário, interface.</li>
        <li>Controle de qualidade: realizado por meio de testes e iterações, com a incorporação de melhorias.</li>
      </ol>
    </li>
    <li><strong>Distribuição:</strong>
      <ol start="9">
        <li>Gerenciamento da comunidade: supervisionar a disponibilização da versão final e sua implementação.</li>
        <li>Suporte: criação de tutoriais e suporte para o estudante e os professores. </li>
        <li>Treinamento: disponibilização de documento e realização de treinamento com professores.</li>
      </ol>
    </li>
  </ul>

    <p>Os autores indicam meios para a construção de protótipos: <em lang="en">wireframing</em>, protótipos de papel, protótipos de uma página, documento de design. Além disso, indicam ferramentas autorais<a class="nota" href="#nota21" id="ref21" title="Ferramentas autorais: a) modelos para jogo: ActiveDen; C3 Softworks’ Bravo; eLearning Brothers; Jeopardy Lab; Knowledge Guru; Raptivity; b) animação de personagem: CodeBaby; Crazy Talk; Media Semantics; NOAH; c) criação de jogos:  Articulate Storyline; Adobe Captivate; JeLSIM Builder; Lectora; Quandary; What2Learn; ZebraZapps (KAPP, BLAIR E MESCH, 2014, p. 276).">21</a> e motores de jogo<a class="nota" href="#nota22" id="ref22" title="Motores de jogo: a) jogos em 2D: YoYo Games GameMaker; GameSalad; Scirra Construct2; Stencyl; b) jogos em 3D: CryEngine; ThinkingWorlds; Torque 3D; Unity3D; Unreal Engine (KAPP, BLAIR E MESCH, 2014, p. 279).">22</a> para o desenvolvimento da solução final. Em relação à validação, é recomentada a realização de testes de jogabilidade tão logo quanto possível e com frequência, para verificar a aprendizagem e usar os resultados para melhorar o jogo durante o desenvolvimento, de modo iterativo e progressivo (KAPP, BLAIR E MESCH, 2014, p. 172).</p>
    <p>Groff <em lang="en">et al</em>. (2015, p. 5) apresentam a abordagem “<em lang="en">Balanced Design”</em>, que agrupa o Modelo de conteúdo, o Modelo de evidência e o Modelo de tarefas. Juntos, esses elementos auxiliam a especificar conhecimentos e habilidades a serem exploradas, estruturar as tarefas para desenvolver conhecimento e habilidades, e organizar dados e evidências, de modo a facilitar a intepretação, após a realização das tarefas.</p>
    <ul>
      <li><strong>Modelo de conteúdo:</strong> define conhecimentos e habilidades almejadas na construção da aprendizagem por meio do jogo e engloba a construção do conteúdo e do nível dos estudantes.</li>
      <li><strong>Modelo de evidência:</strong> descreve observações e comportamentos de estudantes que evidenciam suas habilidades, para interpretar seu desempenho em cada tarefa e entender como o modelo de conteúdo deve ser atualizado, para alinhar o aprendizado.</li>
      <li><strong>Modelo de tarefa:</strong> ddescreve situações (tarefas, experimentos) que evocam ou fornecem as evidências. Envolve descrição de missões, tarefas e ações no jogo.</li>
    </ul>
