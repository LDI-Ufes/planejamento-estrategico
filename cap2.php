  <p>O autor pontua que é papel do designer de jogos facilitar a entrada do jogador no canal de fluxo e dificultar sua saída, por meio da adequação do desafio ao nível de habilidade, durante o jogo. </p>
  <p>Segundo Mattar Neto (2008, p. 6), o uso de jogos possibilita um nível mais profundo e intenso de interatividade – devido ao estado de fluxo –, que proporciona concentração ou completa absorção, motivação e imersão. </p>
  <p>Romão <em lang="en">et al</em>. (2008 p. 161) esclarece, entretanto, que é preciso aumentar a dificuldade progressivamente para que o aluno assimile novas informações gradualmente, conforme avança no jogo. Prensky (2012, p. 180) ratifica que um dos desafios é manter o estudante-jogador em estado de fluxo no jogo e na aprendizagem, ao mesmo tempo.</p>
  <p>Kapp, Blair e Mesch (2014, p. 49) afirmam que há uma diferença entre jogos que ensinam como fazer algo e jogos que testam o que o jogador já sabe, mas que é comum a confusão e o engano ao escolher um dos dois tipos. O primeiro apresenta informações sobre como fazer algo, seguido de uma tarefa, e auxilia o jogador a ajustar comportamento, com base no que o jogo fornece; enquanto o segundo tipo tem a finalidade de fazer o estudante recordar informações já fornecidas em outro contexto, fora do jogo. Além disso, os autores explicam que a escolha do tipo de jogo dependerá de diversos fatores, tais como as limitações projetuais, os recursos e a capacidade projetual da equipe. Outrossim, é preciso pensar em como os tipos de jogo podem combinar os padrões de mecânica aos tipos de conhecimento, habilidades e atitudes (KAPP, BLAIR E MESCH, 2014, p. 208). </p>
  <p>Prensky (2012, p. 220) apresenta 13 tipos de conteúdos de aprendizagem a serem aprendidos (fatos, habilidades, julgamento, comportamentos, teorias, raciocínio, processo, procedimentos, criatividade, linguagem, sistemas, observação e comunicação). Para esses, o autor propõe atividades e algumas opções para aprendê-las, relacionando-as com estilos de jogos possíveis.</p>

