  <p>De acordo com Gee (2007, p. 18), a aprendizagem ativa envolve experiências em relação ao mundo de novas formas, considerando imagens, palavras, símbolos, artefatos, e assim por diante, em aspectos e domínios semióticos, para formar novas conexões e preparar para aprendizagens futuras. Segundo o autor, o próprio ato de jogar implica em obter conhecimentos sobre as regras e possibilidades e, quando postos em prática, geram experiência, aprendizagem ativa e crítica, e estabelecem relações no jogo e fora dele.</p>
  <p>Novak (2010, p. 75) corrobora que jogos educativos são aqueles criados para ensinar, e baseiam-se na aquisição de conhecimentos ao jogar e nos fatores motivacionais essenciais para o aprendizado. Além da obtenção e aplicação de conhecimentos, há outras formas de aprendizado atuando na maioria dos jogos, senão em todos. A autora complementa que estes podem melhorar as habilidades físicas e mentais e cita os <em lang="en">serious games</em>, jogos usados por empresas, entidades, governo, ou pelo terceiro setor para treinar, informar, recrutar ou divulgar.</p>
  <p>Prensky (2012, p. 208) define a Aprendizagem Baseada em Jogos Digitais como “qualquer jogo para o processo de ensino-aprendizagem em um computador ou on-line”, que funciona em razão do envolvimento e do processo interativo. Para o autor, acrescentar diversão ao processo de aprendizagem contribui para que este se torne mais agradável, envolvente e eficaz. A aprendizagem baseada em jogos pode desempenhar um papel importante na interiorização de conteúdos que não motivam de forma intrínseca, mas que precisam ser apreendidos (PRENSKY, 2012, p. 44).</p>
  <p>O autor sintetiza que os jogos divertem, proporcionam prazer, satisfação, envolvimento intenso e passional; as regras promovem estrutura; as metas possibilitam motivação; a interatividade viabiliza a ação; as vitórias gratificam o ego; a interação gera grupos sociais; os enredos emocionam; os resultados e o <em lang="en">feedback</em> proporcionam aprendizagem; os conflitos, desafios, as competições e oposições favorecem adrenalina; e a solução de problemas estimula a criatividade (PRENSKY, 2012, p. 156). Além disso, Prensky (2012, p. 179) explica que, ao jogarmos, alcançamos um estado de concentração intensa, denominado “fluxo” pelo psicólogo Mihaly Csikszentmihalyi. </p>
  <p>Chen (2008, n. p.) afirma que o fluxo é o estado atingido ao encontrar um equilíbrio dinâmico entre o desafio de uma atividade e a habilidade para cumpri-lo. Segundo o autor, há na proposta de Csikszentmihalyi oito elementos principais no fluxo: uma atividade desafiadora que requer habilidades; um misto de ação e consciência; objetivos claros; <em lang="en">feedback</em>; concentração na tarefa; senso de controle; perda de consciência própria; transformação do tempo – embora nem todos esses sejam necessários para ter a experiência de estado de fluxo (CHEN, 2008, n. p.). O autor explica que o jogo deve ser intrinsecamente recompensador e oferecer uma quantidade de desafio que seja compatível com as habilidades do jogador, além de permitir que esse sinta controle sobre a atividade do jogo.</p>
  <p>Tori (2010, p. 98) explica que para entrar e continuar em estado de fluxo ao jogar, é preciso equilibrar as habilidades do jogador e o desafio proposto pelo jogo. Entretanto, caso o desafio seja muito superior às habilidades, o jogador ficará ansioso e desistirá. Caso as habilidades sejam muito maiores que o desafio, o jogador ficará entediado.</p>

