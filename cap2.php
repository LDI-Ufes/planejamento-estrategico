  <p>Garris, Ahlers e Driskell (2002, p. 444) apresentam o modelo “<em lang="en">Input-process-outcome</em>” com o objetivo de motivar o aprendiz por meio do design de um programa instrucional que incorpore características e funcionalidades de jogos. Essa combinação deve resultar em uma jogabilidade motivadora, que impulsiona o ciclo do jogo – o componente-chave –, por meio do qual o jogador o joga muitas vezes, continuamente, em ciclos de interpretação-comportamento-<em lang="en">feedback</em>. </p>
  <p>O momento de julgamento provoca uma reação no jogador, que pode ser proveniente de interesse, satisfação, envolvimento com a tarefa e confiança. Essa reação de julgamento gera comportamento e ação, para os quais o sistema fornece um <em lang="en">feedback</em>, que causa reinterpretação, julgamento e reação, continuamente. Em seguida, esse engajamento no jogo leva a alcançar os objetivos do treinamento e gera os resultados da aprendizagem.</p>
  <p>Por fim, os resultados de aprendizagem gerados podem ser cognitivos, (de conhecimento declarativo, processual e estratégico); baseado em habilidades (relacionados a habilidades técnicas ou motoras); e afetivos (vinculados a atitudes e autoconfiança). Nesse processo, a avaliação é fundamental, para que a experiência no jogo ou simulação seja descrita, analisada, discutida e vinculada ao mundo real, de modo a permitir ao estudante interpretar esses eventos como uma experiência de aprendizagem (GARRIS; AHLERS; DRISKELL, 2002, p. 455).</p>
