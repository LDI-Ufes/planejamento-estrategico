  <p>Nadolski <em lang="en">et al</em>. (2008, p. 2) apresenta a metodologia para o desenvolvimento de <em lang="en">serious games</em> para a educação, com base no modelo ADDIE<a class="nota" href="#nota20" id="ref20" title="ADDIE é a abreviação, na língua inglesa, dos termos análise, design, desenvolvimento, implementação e avaliação – um modelo para o desenvolvimento de materiais instrucionais (NADOLSKI et al., 2008, p. 2).">20</a>, intitulada EMERGO. Trata-se de um método de processo unificado, que ocorre por iterações e possui as seguintes fases: análise, design, desenvolvimento, implementação e avaliação.</p>
  <ol>
    <li><strong>Análise:</strong> etapa de definição dos requisitos do projeto do jogo, do público, da abordagem e contextualização institucional, do curso e conteúdo.</li>
    <li><strong>Design:</strong> fase de descrição do cenário para a decisão e ordenação das atividades, ferramentas e recursos.</li>
    <li><strong>Desenvolvimento:</strong> fase de produção e teste, de modo cíclico, de cada componente do projeto do jogo. </li>
    <li><strong>Implementação:</strong> etapa de distribuição e acesso do jogo, por parte de estudantes e professores.</li>
    <li><strong>Avaliação:</strong> estágio para aferir se a solução cumpriu os requisitos definidos na etapa de análise. É elaborado um plano de avaliação sobre a solução, o conteúdo, o progresso e interação do estudante, além do suporte.</li>
  </ol>
  <p>Westera <em lang="en">et al</em>. (2008, p. 425) complementam que o modelo EMERGO tem três níveis: conceitual, técnico e prático, para reduzir a complexidade do design de jogos educacionais.</p>
